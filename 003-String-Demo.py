# String Documentation https://docs.python.org/3/library/string.html
# String Methods: https://docs.python.org/3/library/stdtypes.html#string.methods

name = "rajeev ranjan"
print(name)

print(name.upper())
print(name.title())

# Formatting String
name = "Rajeev Ranjan"
print(f"Hello  {name}")

first_name = "Rajeev"
last_name = "Ranjan"
print(f"Hello {first_name} {last_name}") # f is for the format string or f-string.

print("Hello" + " " + first_name +  " " + last_name) # Concatenation

print (10 + 20) # Integer addition
print ("10" + "20") # Concatenation